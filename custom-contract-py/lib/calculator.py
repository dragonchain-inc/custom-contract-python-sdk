def addition(param):
    # The value here will be stored on the blockchain
    return int(param["numOne"]) + int(param["numTwo"])


def subtraction(param):
    # The value here will be stored on the blockchain
    return int(param["numOne"]) - int(param["numTwo"])


def multiplication(param):
    # The value here will be stored on the blockchain
    return int(param["numOne"]) * int(param["numTwo"])
